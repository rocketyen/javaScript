var firstName = 'John';
var lastName = 'Doe';
var age = 18;

/* avec magic cotes permet de mettre directement des variables 
avec ${} */
console.log(`Bonjour, je suis ${firstName} ${lastName}, j'ai ${age} ans`);

/* avec simples cotes on ajoute des + entre le texte 
et les variable pour les séparés */
console.log('Bonjour, je suis '+firstName+ ' ' +lastName+ ', j\'ai ' 
+age+ ' ans')




